#include "pch.h"

cpu_rectangle::cpu_rectangle()
{
	Zero();
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void cpu_rectangle::Zero()
{
	minX = 0;
	minY = 0;
	maxX = 0;
	maxY = 0;
}

bool cpu_rectangle::IsEmpty()
{
	if ( minX>=maxX || minY>=maxY )
		return true;
	return false;
}
