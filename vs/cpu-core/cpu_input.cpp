#include "pch.h"

cpu_input::cpu_input()
{
	Reset();
}

cpu_input::~cpu_input()
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

cpu_input& cpu_input::GetInstance()
{
	static cpu_input input;
	return input;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void cpu_input::Update()
{
	bool focus = true;
	if ( pWindow )
		focus = pWindow->HasFocus();

	for ( int i=1 ; i<255 ; i++ )
	{
		bool state = false;
		if ( focus )
			state = GetAsyncKeyState(i)<0;

		if ( state )
		{
			switch ( keys[i] )
			{
			case _NONE:
			case _UP:
				keys[i] = _DOWN;
				break;
			case _DOWN:
				keys[i] = _PUSH;
				break;
			}
		}
		else
		{
			switch ( keys[i] )
			{
			case _PUSH:
			case _DOWN:
				keys[i] = _UP;
				break;
			case _UP:
				keys[i] = _NONE;
				break;
			}
		}
	}
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void cpu_input::Reset()
{
	memset(keys, 0, 256);
	pWindow = nullptr;
}

void cpu_input::SetWindow(cpu_window* pWnd)
{
	pWindow = pWnd;
}

bool cpu_input::IsKey(int key)
{
	return keys[key]==_DOWN || keys[key]==_PUSH;
}

bool cpu_input::IsKeyUp(int key)
{
	return keys[key]==_UP;
}

bool cpu_input::IsKeyDown(int key)
{
	return keys[key]==_DOWN;
}
